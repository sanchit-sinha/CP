void solve(){
    ll n , m ,r , c;
    cin >> n >> m >> r >> c;
    pll x[4] ;
    x[0] = {1,1};
    x[1] = {1,m};
    x[2] = {n,1};
    x[3] = {n,m};
    ll answer =0 ;
    for(ll i=0;i<4;i++){
    	ll xx = x[i].f , yy=x[i].s;
    	answer = max(answer , abs(xx-r) + abs(yy-c));
    }
    cout<<answer<<"\n";
}

